package DataTypes;

public class PrimitiveDataType {
    /*
    Boolean data type
    Byte data type
    short data type
    int data type
    long data type
    Float data type
    Double data type
    Char data type
    */

    public static void main(String[] args) {
        boolean one=false;
        System.out.println(one);


        byte a=10,b=20;
        int i = a + b;
        System.out.println(i);

        short x=10;
        System.out.println(x);

        long y=10000l;
        System.out.println(y);

        float z=10.92f;
        System.out.println(z);

        double d=12.6575;
        System.out.println(d);

        char c='a';
        System.out.println(c);




    }
}
